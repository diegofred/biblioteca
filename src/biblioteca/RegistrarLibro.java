    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

        String titulo = txtTituloLibro.getText();

        String autor = txtAutorLibro.getText();

        int anio = Integer.parseInt(txtAnio.getText());

        int opcionTipoLibroSeleccioda = cmbTipoLibro.getSelectedIndex();

        if (opcionTipoLibroSeleccioda >= 0) {

            TipoLibro tipos = (TipoLibro) Biblioteca.tiposLibro.get(opcionTipoLibroSeleccioda);

            Libro l1 = new Libro(autor, titulo, anio, tipos);

            //Guardar la persona
            JOptionPane.showMessageDialog(null, "Libro creado " + l1.toString());

            this.setVisible(false);
            this.dispose();

        } else { //Opcion -1
            JOptionPane.showMessageDialog(null, "Debe seleccionar un tipo de libro");
        }

    }//GEN-LAST:event_btnRegistrarActionPerformed

