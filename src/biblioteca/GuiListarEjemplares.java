        private void prepararInterfaz() {
        DefaultTableModel tm = (DefaultTableModel) tbListadoEjemplares.getModel();

        //actual data for the table in a 2d array
        int cantidadLibros = Biblioteca.libros.size();
        int cantidadColumnas = tm.getColumnCount();

        for (int i = 0; i < cantidadLibros; i++) {
            Libro l = (Libro) Biblioteca.libros.get(i);
            Object[] fila = new Object[cantidadColumnas];
            
            //Estas filas deberian concidir en orden con las declaras en la tabla
            fila[0] = l.titulo;
            fila[1] = l.autor;
            fila[2] = l.anio;
            fila[3] = l.cantidadEjemplaresDisponibles();


            tm.addRow(fila);
        }

    }

